package com.basic1;

public class AccessModifiers {
	
	//public :- visibility is through out the project
	//private:- visibility is in same class
	//default:- visibility is in the same package
	//protected :- visibility is same package and outside package in child class
		
	
	public String publicVariable = "I am public variable";
	private String privateVariable = "I am private variable";
	String defaultVariable = "I am default variable";
	protected String protectedVariable = "I am protected variable";

}